package day03_scanner;

public class C01AsciiTable {
    public static void main(String[] args) {
        int sayi=10;
        char harf='a';
        String str="banan";

        System.out.println(sayi+harf);

        System.out.println(sayi*harf);
        System.out.println(str+harf);
        System.out.println(str+sayi+harf);
    }
}
